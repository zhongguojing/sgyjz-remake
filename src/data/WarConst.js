// ==================== 兵种相克 ====================
// 翻译自 BZSuper() (jymain.lua:4814)
// 返回 true 表示 bz1 克制 bz2
export function bzSuper(bz1, bz2) {
  // 原版克制链: 骑兵>步兵>弓兵>骑兵
  // 扩展: 重骑兵>重步兵, 铁甲骑>步兵/弓兵, 弩兵>步兵 等
  const advantageTable = {
    // 骑兵系 克制 步兵系
    1: [0, 6],      // 骑兵 克制 步兵, 重步兵
    7: [0, 6, 2],   // 重骑兵 克制 步兵, 重步兵, 弓兵
    9: [0, 6, 2, 8],// 铁甲骑 克制 步兵, 重步兵, 弓兵, 弩兵
    // 步兵系 克制 弓兵系
    0: [2, 8],      // 步兵 克制 弓兵, 弩兵
    6: [2, 8],      // 重步兵 克制 弓兵, 弩兵
    // 弓兵系 克制 骑兵系
    2: [1, 7],      // 弓兵 克制 骑兵, 重骑兵
    8: [1, 7],      // 弩兵 克制 骑兵, 重骑兵
    // 特殊克制
    11: [3, 16, 17],// 水军 克制 军师, 策士, 医师
    15: [0, 6, 1],  // 象兵 克制 步兵, 重步兵, 骑兵
  };
  return advantageTable[bz1]?.includes(bz2) ?? false;
}

